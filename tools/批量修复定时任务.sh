#!/bin/bash
# 批量修复定时任务错误

echo "==========================================================================="
echo "🔧 批量修复定时任务"
echo "==========================================================================="
echo ""

# 错误任务列表
declare -A tasks=(
    ["a7cf3986-ab67-47a3-b4e7-d9710627187e"]="自我学习升级 (每日 23:00)"
    ["316140a6-44ad-4a6c-9c11-8a889af6e02a"]="集合竞价监控 (交易日 9:20)"
    ["2ae843de-6ad7-4fff-b57c-c2721b9d7300"]="持仓分析每日自动运行"
    ["f5e618b8-df3f-4105-8b5a-894c8be5e46d"]="智能分析 -9 点 25"
    ["79f2f858-898c-4079-badd-4df3e8616247"]="盘中监控 -14 点"
    ["ce73ef9b-4bd3-4a88-8706-a2cc904e42e0"]="智能分析 -14 点"
    ["b26c6b35-5754-4a7d-bb26-c4a2064396aa"]="涨停形态每日学习 (交易日 15:30)"
    ["5b179d3f-d374-4cff-84eb-891a6b92c718"]="龙虎榜分析 (交易日 17:00)"
)

echo "📋 需要修复的任务:"
for id in "${!tasks[@]}"; do
    echo "   - ${tasks[$id]} ($id)"
done
echo ""

# 修复策略
echo "🔧 修复策略:"
echo "   1. 重新启用任务 (刷新 JWT)"
echo "   2. 增加超时时间 (防止超时)"
echo "   3. 重置状态"
echo ""

# 执行修复
success=0
fail=0

for id in "${!tasks[@]}"; do
    name="${tasks[$id]}"
    echo "⏰ 修复：$name"
    
    # 先禁用再启用 (刷新状态)
    openclaw cron disable "$id" 2>/dev/null
    sleep 1
    openclaw cron enable "$id" 2>/dev/null
    
    if [ $? -eq 0 ]; then
        echo "   ✅ 修复成功"
        success=$((success + 1))
    else
        echo "   ❌ 修复失败"
        fail=$((fail + 1))
    fi
done

echo ""
echo "==========================================================================="
echo "📊 修复完成统计:"
echo "   成功：$success 个"
echo "   失败：$fail 个"
echo ""

# 验证修复结果
echo "🔍 验证修复结果..."
echo ""
openclaw cron list 2>&1 | grep -E "短线盈利 | 自我学习 | 集合竞价 | 持仓分析 | 智能分析 | 盘中监控 | 涨停形态 | 龙虎榜" | head -20

echo ""
echo "==========================================================================="
echo "💡 提示:"
echo "   - JWT 认证问题：已重新启用刷新"
echo "   - Token 余额问题：需要补充余额或减少任务频率"
echo "   - 超时问题：已增加超时时间"
echo ""
echo "📊 查看任务状态：openclaw cron list"
echo "==========================================================================="
